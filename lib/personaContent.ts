import type { ZhouliLevel, ZhouliMode } from "./prompt";

export type Persona = "confucius" | "laozi" | "zen";

const VALID_PERSONAS = new Set<Persona>(["confucius", "laozi", "zen"]);

export function parsePersona(value: unknown): Persona {
  if (typeof value === "string" && VALID_PERSONAS.has(value as Persona)) {
    return value as Persona;
  }
  return "confucius";
}

export function guardLabel(persona: Persona): string {
  if (persona === "laozi") return "道官校订";
  if (persona === "zen") return "禅官校订";
  return "礼官校订";
}

export function guardLabelStrict(persona: Persona): string {
  if (persona === "laozi") return "道官守门";
  if (persona === "zen") return "禅官守门";
  return "礼官守门";
}

export function demoLabel(persona: Persona): string {
  if (persona === "laozi") return "道法演示";
  if (persona === "zen") return "禅机演示";
  return "本地演示";
}

function pick<T>(items: readonly T[]) {
  return items[Math.floor(Math.random() * items.length)];
}

